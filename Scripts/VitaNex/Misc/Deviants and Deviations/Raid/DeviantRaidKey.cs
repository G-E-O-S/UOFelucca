namespace Server.Items
{
	public class DeviantRaidKey : Item
	{
		public override string DefaultName => "Deviant Raid Key";

		[Constructable]
		public DeviantRaidKey()
			: base(0x1AE1)
		{ }

		public DeviantRaidKey(Serial serial)
			: base(serial)
		{ }

		public override void Serialize(GenericWriter writer)
		{
			base.Serialize(writer);

			writer.SetVersion(0);
		}

		public override void Deserialize(GenericReader reader)
		{
			base.Deserialize(reader);

			reader.GetVersion();
		}
	}
}
